			<h1 id="AboutSyncGallery">About SyncGallery+</h1>
			<p>
			SyncGallery+, developed by Team 1, is a native application designed for the Android Platform. 
			</p>
			<p>
			SyncGallery+ uses Dropbox as its cloud storage, which provides reliable online storage, true cross-platform support and simple sharing of files and directories. Dropbox provides developers with the API for the common usage of Dropbox such as upload and download, making the cloud storage operations easy to implement. Aviary, which provides the most popular photo-editing functions, is also integrated into SyncGallery+. About 80% of the photo editing feature requirement of the project is ready-to-use in Aviary and its User Interface is designed to look suitable within any existing application. In addition, creative customization is allowed for any developers looking for a unique look in their applications. The interaction with social networking services is done using the built-in share functionality of Android. The throw-and-bump feature is done via Bump's API. 
			</p>
			<p>
			For developers seeking a product for enhancement, SyncGallery+ is a wise choice as a number of the third party tools/applications now have API for Android. Most of the time, only integration of different components is required. Furthermore, it's much easier to test the product as majority of SyncGallery+'s feature doesn't require network access.
			</p>
			<p>
			With SyncGallery+, you can take a picture and immediately start adding your personal taste to it. Or, you can also edit any pictures in your gallery. SyncGallery+ is a native application so it ensures  excellent performance with no lag time and its photo-editing feature will remain operable even without network signal. It takes only 10 seconds to go through a simply registration to have your favourite pictures synced real-time, whether they are on your cell phone, computer, or tablet. In addition, SyncGallery+ offers 2 great ways to share pictures with your friends and families. You can either share via social networking services (such as Facebook, Twitter, and Google+) or you can simply throw and bump! (This feature will be demonstrated in our video and in class)
			</p>
